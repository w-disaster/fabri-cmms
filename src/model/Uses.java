package model;

public class Uses {

	private String codSupplier;
	private String billNumber;
	private String itemSerialNumber;
	private String numSparePart;
	private String description;
	private String amount;
	
	public String getItemSerialNumber() {
		return itemSerialNumber;
	}
	public void setItemSerialNumber(String itemSerialNumber) {
		this.itemSerialNumber = itemSerialNumber;
	}
	public String getCodSupplier() {
		return codSupplier;
	}
	public void setCodSupplier(String codSupplier) {
		this.codSupplier = codSupplier;
	}
	public String getBillNumber() {
		return billNumber;
	}
	public void setBillNumber(String billNumber) {
		this.billNumber = billNumber;
	}
	public String getNumSparePart() {
		return numSparePart;
	}
	public void setNumSparePart(String numSparePart) {
		this.numSparePart = numSparePart;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	
}
